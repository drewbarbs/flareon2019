#include <immintrin.h>

char r3init[32] = {0x15, 0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x13, 0x1a,
                   0x1b, 0x1b, 0x1b, 0x1a, 0x15, 0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x11,
                   0x11, 0x11, 0x13, 0x1a, 0x1b, 0x1b, 0x1b, 0x1a};
char flag[32] = {0};

int main(int argc, char **argv) {
  __m256i a = _mm256_loadu_si256((__m256i const *)r3init);
  __m256i b = _mm256_loadu_si256((__m256i const *)flag);

  __m256i fullcmp = _mm256_cmpeq_epi8(a, b);

  int32_t result = _mm256_movemask_epi8(fullcmp);

  if (result == -1) {
      return 0;
  } else {
      return 1;
  }
}
